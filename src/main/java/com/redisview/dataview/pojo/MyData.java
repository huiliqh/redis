package com.redisview.dataview.pojo;

import lombok.ToString;

@lombok.Data
@ToString
public class MyData {
    private String key;
    private String value;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
